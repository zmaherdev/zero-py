print(type(10)) # Integer
print(type(1000)) # Integer
print(type(-40)) # Integer

print(type(-40.5)) # Floating Point Number
print(type(4.929925)) # Floating Point Number
print(type(4000.112)) # Floating Point Number

print(type("Hello there!")) # String

print(type( [1,2,3,5] )) # List

print(type( (1,2,3,5) )) # Tuple

print(type( {"One": 1, "Two": 2, "Three": 3} )) # Dictionary

print(type(2 == 2)) # Boolean
print(type(True)) # Boolean
print(type(False)) # Boolean
